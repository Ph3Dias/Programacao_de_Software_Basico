ATRASO:
	LDI R24, 0X0

	LDI R20, 53

L1:	LDI R21, 2;

L2:	LDI R22, 250;

L3: LDI R23, 200 ;

L4: DEC R23;1
	BRNE L4;2

	DEC R22;1
	BRNE L3;2

	DEC R21;1
	BRNE L2;2

	CPI R24, 0X0
	BREQ LS
	CPI R24, 0XFF
	BREQ LM


A:  DEC R20
	BRNE L1
	INC R16
	RJMP PRINCIPAL

LS:
	CBI PORTB, PB1
	SBI PORTB, PB2
	COM R24
	OUT PORTD, R17
	RJMP A
LM:
	CBI PORTB, PB2
	SBI PORTB, PB1
	COM R24
	OUT PORTD, R19
	RJMP A


	

	

	

